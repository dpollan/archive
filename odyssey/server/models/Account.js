const mongoose = require('mongoose');

const AccountSchema = mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
});

const Account = mongoose.model('account', AccountSchema);

module.exports = Account;
